public class ReportPojo {

    int cases;
    int totalTests;
    int pcrTests;
    int rapidTests;
    int positivityPercentage;
    int deaths;
    int intubatedPatients;

    public int getCases() {
        return cases;
    }

    public void setCases(int cases) {
        this.cases = cases;
    }

    public int getTotalTests() {
        return totalTests;
    }

    public void setTotalTests(int totalTests) {
        this.totalTests = totalTests;
    }

    public int getPcrTests() {
        return pcrTests;
    }

    public void setPcrTests(int pcrTests) {
        this.pcrTests = pcrTests;
    }

    public int getRapidTests() {
        return rapidTests;
    }

    public void setRapidTests(int rapidTests) {
        this.rapidTests = rapidTests;
    }

    public int getPositivityPercentage() {
        return positivityPercentage;
    }

    public void setPositivityPercentage(int positivityPercentage) {
        this.positivityPercentage = positivityPercentage;
    }

    public int getDeaths() {
        return deaths;
    }

    public void setDeaths(int deaths) {
        this.deaths = deaths;
    }

    public int getIntubatedPatients() {
        return intubatedPatients;
    }

    public void setIntubatedPatients(int intubatedPatients) {
        this.intubatedPatients = intubatedPatients;
    }

}
